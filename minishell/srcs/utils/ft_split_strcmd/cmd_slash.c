#include "minishell.h"

void	ft_count_word_slash(char *s, int *i, int *n)
{
	int count;
	int save;

	count = 0;
	save = *i;
	while (s[*i] && s[*i] == '\\')
	{
		(*i)++;
		count++;
	}
	if ((count % 2) == 0)
		(*n)++;
	else if ((count % 2) == 1 && count > 1)
	{
		i--;
		(*n)++;
	}
	if  ((count % 2) == 1 && count > 1 && (s[*i + 1] == '\'' || s[*i + 1] == '"' || s[*i] == '"' || s[*i] == '\''))
	{
		(*i)++;
		(*n)++;
	}
	else
	{
		while (s[*i] && s[*i] != ' ' && s[*i] != '"' && s[*i] != '\'' && s[*i] != '$' && s[*i] != '\\')
			(*i)++;
		(*n)++;
	}
}