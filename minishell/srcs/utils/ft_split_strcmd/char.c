#include "minishell.h"

void	ft_count_word_char(char *s, int *i, int *n)
{
	while (s[*i] != '\0' && s[*i] != ' ' && s[*i] != '"' && s[*i] != '\'' && s[*i] != '\\' && s[*i] != '$' && s[*i] != '>' && s[*i] != '<' && s[*i] != '|')
			(*i)++;
		(*n)++;
}