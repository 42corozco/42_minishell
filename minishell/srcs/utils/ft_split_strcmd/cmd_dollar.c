#include "minishell.h"

void	ft_count_word_dollar(char *s, int *i, int *n)
{
	int count;

	count = *n;
	(*i)++;
	if (s[*i] == '?')
	{
		(*i)++;
		(*n)++;
	}
	else if (s[*i] == '"' || s[*i] == '\'')
		*n = adeline(s, s[*i], count, i);
	else if (s[*i] == ' ' || s[*i] == '\0' || s[*i] == '\\')
		(*n)++;
	else
	{
		while (s[*i] && ((s[*i] > 47 && s[*i] < 58) || (s[*i] > 64 && s[*i] < 91) || (s[*i] > 96 && s[*i] < 123)))
			(*i)++;
		(*n)++;
	}
}
